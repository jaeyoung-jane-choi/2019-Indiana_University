def how_many_vocals(word):
    """This function returns the number of vocals in the inputed string (uppercase or lowercase)

    string-> number"""

    word= word.lower()
    result1 = word.count('a')
    result2 = word.count('e')
    result3 = word.count('i')
    result4 = word.count('o')
    result5 = word.count('u')

    total_result = result1 +result2+result3+result4+result5
    return total_result 
    

